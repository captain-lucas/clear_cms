
ClearCms.Content = DS.Model.extend({
  // primaryKey: '_id',
  basename: DS.attr('string'),
  content_blocks: DS.hasMany('content_block', {embedded: 'always'}),
  title: DS.attr('string'),
  _type: DS.attr('string')
});

ClearCms.ContentBlock = DS.Model.extend({
  primaryKey: '_id',
  content: DS.belongsTo('content',{embedded: 'always'}),
  body: DS.attr('string'),
  content_assets: DS.hasMany('content_asset', {embedded: 'always'}),
});

